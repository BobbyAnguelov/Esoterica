    static bool CleanDirectory( FileSystem::Path const& directoryPath, bool silentMode )
    {
        if ( FileSystem::IsExistingDirectory( directoryPath ) )
        {
            if ( !FileSystem::EraseDir( directoryPath ) )
            {
                if ( !silentMode )
                {
                    PrintError( "Failed erasing directory: %s", directoryPath.c_str() );
                }

                return false;
            }

            if ( !FileSystem::CreateDir( directoryPath ) )
            {
                if ( !silentMode )
                {
                    PrintError( "Failed creating directory: %s", directoryPath.c_str() );
                }

                return false;
            }
        }

        return true;
    };

    static bool CleanFile( FileSystem::Path const& filePath, char const * const pLabel, bool silentMode )
    {
        if ( FileSystem::Exists( filePath ) )
        {
            if ( FileSystem::EraseFile( filePath ) )
            {
                if ( !silentMode )
                {
                    std::cout << "  Cleaning " << pLabel << " - " << filePath.c_str() << std::endl;
                }
            }
            else
            {
                if ( !silentMode )
                {
                    PrintError( "Failed erasing file: %s", filePath.c_str() );
                }

                return false;
            }
        }

        return true;
    };

    static bool IsShaderParametersFilename( FileSystem::Path const& filePath )
    {
        if ( !filePath.IsValid() || !filePath.IsFilePath() )
        {
            return false;
        }

        String const filename = filePath.GetFilename();
        return StringUtils::EndsWith( filename.c_str(), Settings::g_reflectedParametersFilenameSuffixAndExtension );
    }

    static bool DoTextFileContentsDiffer( FileSystem::Path const& filePath, String const& expectedContents )
    {
        EE_ASSERT( filePath.IsValid() && filePath.IsFilePath() );
        EE_ASSERT( !expectedContents.empty() );

        bool shouldUpdateFile = false;
        if ( Exists( filePath ) )
        {
            String currentFileContents;
            if ( !FileSystem::ReadTextFile( filePath, currentFileContents ) )
            {
                return true;
            }

            if ( currentFileContents != expectedContents )
            {
                return true;
            }

            return false;
        }
        else
        {
            return true;
        }
    }

    static char const* GetStringForOutput( Reflector::Output output )
    {
        static constexpr char const * const outputNames[] =
        {
            "Shaders and TypeInfo",
            "Shaders",
            "TypeInfo",
        };

        return outputNames[int32_t( output )];
    }

    static char const* GetStringForOperation( Reflector::Operation operation )
    {
        static constexpr char const * const operationNames[] =
        {
            "Incremental Build",
            "Rebuild",
            "Clean",
            "Shader Hot-Reload"
        };

        return operationNames[int32_t( operation )];
    }

