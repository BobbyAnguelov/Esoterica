    bool Reflector::ParseSolution()
    {
        m_projects.clear();
        m_shaderReflectionRequiresTypeReflection = false;

        //-------------------------------------------------------------------------

        std::ifstream slnFile( m_solutionPath );
        if ( !slnFile.is_open() )
        {
            return PrintError( "Could not open solution: %s", m_solutionPath.c_str() );
        }

        //-------------------------------------------------------------------------

        auto IsUnderToolsPath = [] ( FileSystem::Path const& path )
        {
            String const& filePathStr = path.GetString();
            for ( auto pToolsProjectName : Settings::g_toolProjectFolderNames )
            {
                InlineString const searchStr( InlineString::CtorSprintf(), "\\%s\\", pToolsProjectName );
                if ( filePathStr.find( searchStr.c_str() ) != String::npos )
                {
                    return true;
                }
            }

            return false;
        };

        static char const* projectPathPrefix = " Path=\"";
        static size_t const projectPathPrefixLength = strlen( projectPathPrefix );

        std::string stdLine;
        while ( std::getline( slnFile, stdLine ) )
        {
            String line( stdLine.c_str() );

            if ( line.find( "<Project" ) != String::npos ) // VS Project UID
            {
                auto projectPathStartIdx = line.find( projectPathPrefix );
                EE_ASSERT( projectPathStartIdx != std::string::npos );
                projectPathStartIdx += projectPathPrefixLength;
                auto projectPathEndIdx = line.find( "\"", projectPathStartIdx );
                EE_ASSERT( projectPathEndIdx != std::string::npos );

                String const projectPathString = line.substr( projectPathStartIdx, projectPathEndIdx - projectPathStartIdx );
                FileSystem::Path const projectPath = m_solutionDirectoryPath + projectPathString;

                //-------------------------------------------------------------------------

                ReflectedProject& project = m_projects.emplace_back();
                project.m_ID = ReflectedProject::GetProjectID( projectPath );
                project.m_path = projectPath;
                project.m_directoryPath = projectPath.GetParentDirectory();
                project.m_isToolsProject = IsUnderToolsPath( projectPath );

                // Generated output paths
                {
                    project.m_autogeneratedDirectoryPath = project.m_directoryPath;
                    project.m_autogeneratedDirectoryPath.Append( Settings::g_moduleDirectoryName, true );
                    project.m_autogeneratedDirectoryPath.Append( Settings::g_autogeneratedDirectoryName, true );

                    project.m_autogeneratedTypeInfoDirectoryPath = project.m_autogeneratedDirectoryPath;
                    project.m_autogeneratedTypeInfoDirectoryPath.Append( Settings::g_autogeneratedTypeInfoDirectoryName, true );

                    project.m_autogeneratedShaderDirectoryPath = project.m_autogeneratedDirectoryPath;
                    project.m_autogeneratedShaderDirectoryPath.Append( Settings::g_autogeneratedShaderDirectoryName, true );

                    project.m_autogeneratedShaderReflectionDirectoryPath = project.m_autogeneratedDirectoryPath;
                    project.m_autogeneratedShaderReflectionDirectoryPath.Append( Settings::g_autogeneratedShaderReflectionDirectoryName, true );
                }

                // Check against shader projects
                for ( char const* const pAllowedShaderProjectName : Settings::g_allowedShaderProjectNames )
                {
                    if ( project.m_path.ToString().find( pAllowedShaderProjectName ) != String::npos )
                    {
                        project.m_isExcludedFromShaderReflection = false;
                        break;
                    }
                }

                // Check against typeinfo projects
                for ( char const* const pAllowedTypeInfoProjectName : Settings::g_allowedTypeInfoProjectNames )
                {
                    if ( project.m_path.ToString().find( pAllowedTypeInfoProjectName ) != String::npos )
                    {
                        project.m_isExcludedFromTypeInfoReflection = false;
                        break;
                    }
                }
            }
        }

        slnFile.close();

        // Parse all relevant projects
        //-------------------------------------------------------------------------

        for ( auto& project : m_projects )
        {
            if ( project.IsRelevantForReflection() )
            {
                if ( !ParseProject( project ) )
                {
                    return false;
                }
            }
            else
            {
                project.m_name = project.m_path.GetFilenameWithoutExtension();
            }
        }

        // Sort projects by dependencies
        //-------------------------------------------------------------------------

        int32_t const numProjects = (int32_t) m_projects.size();
        if ( numProjects > 1 )
        {
            // Create list to sort
            TVector<TopologicalSorter::Node > list;
            for ( auto p = 0; p < numProjects; p++ )
            {
                list.push_back( TopologicalSorter::Node( p ) );
            }

            for ( auto p = 0; p < numProjects; p++ )
            {
                int32_t const numDependencies = (int32_t) m_projects[p].m_dependencies.size();
                for ( auto d = 0; d < numDependencies; d++ )
                {
                    for ( auto pp = 0; pp < numProjects; pp++ )
                    {
                        if ( p != pp && m_projects[pp].m_ID == m_projects[p].m_dependencies[d] )
                        {
                            list[p].m_children.push_back( &list[pp] );
                        }
                    }
                }
            }

            // Try to sort
            if ( !TopologicalSorter::Sort( list ) )
            {
                return false;
            }

            // Update project list
            uint32_t depValue = 0;
            TVector<ReflectedProject> sortedProjects;
            sortedProjects.reserve( numProjects );

            for ( auto& node : list )
            {
                sortedProjects.push_back( m_projects[node.m_ID] );
                sortedProjects.back().m_dependencyCount = depValue++;
            }
            m_projects.swap( sortedProjects );
        }

        return true;
    }

    bool Reflector::ParseProject( ReflectedProject& project )
    {
        std::ifstream prjFile( project.m_path );
        if ( !prjFile.is_open() )
        {
            PrintError( "Could not open project file: %s", project.m_path.c_str() );
            return false;
        }

        TVector<FileSystem::Path> headerFiles;
        TVector<FileSystem::Path> shaderFiles;

        std::string stdLine;
        while ( std::getline( prjFile, stdLine ) )
        {
            String line( stdLine.c_str() );

            // Name
            auto firstIdx = line.find( "<ProjectName>" );
            if ( firstIdx != String::npos )
            {
                firstIdx += 13;
                project.m_name = line.substr( firstIdx, line.find( "</Project" ) - firstIdx );
                continue;
            }

            // Dependencies
            firstIdx = line.find( "<ProjectReference Include=\"" );
            if ( firstIdx != String::npos )
            {
                firstIdx += 27;
                String dependencyPath = line.substr( firstIdx, line.find( "\">" ) - firstIdx );
                dependencyPath = project.m_directoryPath + dependencyPath;
                StringID dependencyID = ReflectedProject::GetProjectID( dependencyPath );
                project.m_dependencies.push_back( dependencyID );
                continue;
            }

            // Header Files
            firstIdx = line.find( "<ClInclude" );
            if ( firstIdx != String::npos )
            {
                firstIdx = line.find( "Include=\"" );
                EE_ASSERT( firstIdx != String::npos );
                firstIdx += 9;
                auto secondIdx = line.find( "\"", firstIdx );
                EE_ASSERT( secondIdx != String::npos );

                String const headerPathStr = line.substr( firstIdx, secondIdx - firstIdx );

                // Ignore auto-generated files
                if ( headerPathStr.find( Settings::g_autogeneratedDirectoryName ) != String::npos )
                {
                    continue;
                }

                headerFiles.emplace_back( project.m_directoryPath + headerPathStr );
            }

            // Shader files
            //-------------------------------------------------------------------------

            firstIdx = line.find( "<None" );
            if ( firstIdx != String::npos )
            {
                firstIdx = line.find( "Include=\"" );
                EE_ASSERT( firstIdx != String::npos );
                firstIdx += 9;
                auto secondIdx = line.find( "\"", firstIdx );
                EE_ASSERT( secondIdx != String::npos );

                String const shaderFilePathStr = line.substr( firstIdx, secondIdx - firstIdx );
                if ( shaderFilePathStr.find( Settings::g_shaderSourceFileExtension ) != String::npos )
                {
                    shaderFiles.emplace_back( project.m_directoryPath + shaderFilePathStr );
                }
            }
        }

        prjFile.close();

        // Name Validation
        //-------------------------------------------------------------------------

        if ( project.m_name.empty() )
        {
            PrintError( "Invalid project file detected: %s", project.m_path.c_str() );
            return false;
        }

        project.m_isBaseProject = ( project.m_name == Settings::g_baseProjectName );

        // Parse Headers
        //-------------------------------------------------------------------------

        project.m_headerFiles.reserve( headerFiles.size() );

        for ( auto const& headerPath : headerFiles )
        {
            // Parse header
            if ( !ParseHeader( project, headerPath ) )
            {
                PrintError( "Error parsing header: %s", headerPath.c_str() );
                return false;
            }
        }

        // Read shaders
        //-------------------------------------------------------------------------

        for ( auto const& shaderPath : shaderFiles )
        {
            ReflectedShader& shader = project.m_shaders.emplace_back();
            shader.m_path = shaderPath;
            shader.m_name = shaderPath.GetFilenameWithoutExtension();

            InlineString filename( InlineString::CtorSprintf(), "%s.cpp", shader.m_name.c_str() );
            shader.m_compiledShaderPath = project.m_autogeneratedShaderDirectoryPath.GetAppended( filename.c_str() );

            filename = InlineString( InlineString::CtorSprintf(), "%s%s", shader.m_name.c_str(), Settings::g_reflectedParametersFilenameSuffixAndExtension );
            shader.m_reflectedParametersFilePath = project.m_autogeneratedShaderDirectoryPath.GetAppended( filename.c_str() );
        }

        return true;
    }

    bool Reflector::ParseHeader( ReflectedProject& project, FileSystem::Path const& path )
    {
        EE_ASSERT( path.IsValid() && path.IsFilePath() );

        // Create reflected header
        ReflectedHeader& header = project.m_headerFiles.emplace_back();
        header.m_path = path;
        header.m_ID = ReflectedHeader::GetHeaderID( header.m_path );
        header.m_projectID = project.m_ID;
        header.m_timestamp = FileSystem::GetFileModifiedTime( header.m_path );
        header.m_isToolsOnlyHeader = project.m_isToolsProject;

        // Check if this is a special header
        enum HeaderType { Default, API, Module };

        HeaderType headerType = HeaderType::Default;

        if ( header.m_path.GetParentDirectory().GetDirectoryName() == Settings::g_moduleDirectoryName )
        {
            if ( header.m_path.IsFilenameEqual( Settings::g_moduleAPIFilename ) )
            {
                headerType = HeaderType::API;
            }
            else if ( StringUtils::EndsWith( header.m_path.GetFilename(), Settings::g_moduleFileSuffix ) )
            {
                headerType = HeaderType::Module;
            }
        }

        // Set up type-info output path
        EE_ASSERT( project.m_autogeneratedTypeInfoDirectoryPath.IsDirectoryPath() );
        InlineString const filenameStr( InlineString::CtorSprintf(), "%s.%s.%s", header.m_path.GetFilenameWithoutExtension().c_str(), Settings::g_typeinfoFileSuffix, "cpp" );
        header.m_typeInfoPath = project.m_autogeneratedTypeInfoDirectoryPath.GetAppended( filenameStr.c_str() );

        //-------------------------------------------------------------------------

        // Open header file
        std::ifstream hdrFile( header.m_path, std::ios::in | std::ios::binary | std::ios::ate );
        if ( !hdrFile.is_open() )
        {
            PrintError( "Could not open header file: %s", header.m_path.c_str() );
            project.m_headerFiles.pop_back();
            return false;
        }

        // Check file size
        uint32_t const size = (uint32_t) hdrFile.tellg();
        if ( size == 0 )
        {
            hdrFile.close();
            project.m_headerFiles.pop_back();
            return true;
        }
        hdrFile.seekg( 0, std::ios::beg );

        // Read file contents
        std::string stdLine;
        while ( std::getline( hdrFile, stdLine ) )
        {
            header.m_fileContents.emplace_back( stdLine.c_str() );
        }
        hdrFile.close();

        //-------------------------------------------------------------------------

        // Check for the EE registration macros
        uint32_t openCommentBlock = 0;

        for ( auto const& line : header.m_fileContents )
        {
            // Check for comment blocks
            if ( line.find( "/*" ) != String::npos ) openCommentBlock++;
            if ( line.find( "*/" ) != String::npos ) openCommentBlock--;

            if ( openCommentBlock == 0 )
            {
                // Check for line comment
                auto const foundCommentIdx = line.find( "//" );

                // Check for registration macros
                for ( auto i = 0u; i < (uint32_t) ReflectionMacro::NumMacros; i++ )
                {
                    ReflectionMacro const macro = (ReflectionMacro) i;
                    auto const foundMacroIdx = line.find( GetReflectionMacroText( macro ) );
                    bool const macroExists = foundMacroIdx != String::npos;
                    bool const uncommentedMacro = foundCommentIdx == String::npos || foundCommentIdx > foundMacroIdx;

                    if ( macroExists && uncommentedMacro )
                    {
                        // We should never have registration macros and the export definition in the same file
                        if ( headerType == HeaderType::API )
                        {
                            PrintError( "EE registration macro found in the module export API header(%)!", header.m_path.c_str() );
                            project.m_headerFiles.pop_back();
                            return false;
                        }

                        // We have a valid registration macro and so this file needs to be processed
                        if ( headerType == HeaderType::Module )
                        {
                            if ( project.m_moduleHeaderID.IsValid() )
                            {
                                PrintError( "Multiple module headers found!" );
                                return false;
                            }

                            project.m_moduleHeaderID = header.m_ID;
                        }
                        return true;
                    }
                }

                // Check header for the module export definition
                if ( headerType == HeaderType::API )
                {
                    auto const foundExportIdx0 = line.find( "__declspec" );
                    auto const foundExportIdx1 = line.find( "dllexport" );
                    if ( foundExportIdx0 != String::npos && foundExportIdx1 != String::npos )
                    {
                        if ( !header.m_exportMacro.empty() )
                        {
                            PrintError( "Duplicate export macro definitions found!", header.m_path.c_str() );
                            return false;
                        }
                        else
                        {
                            auto defineIdx = line.find( "#define" );
                            if ( defineIdx != String::npos )
                            {
                                defineIdx += 8;
                                header.m_exportMacro = line.substr( defineIdx, foundExportIdx0 - 1 - defineIdx );
                            }

                            // Early out for module headers once we've read the export macro
                            if ( !project.m_exportMacro.empty() )
                            {
                                PrintError( "Multiple module API headers found!" );
                                project.m_headerFiles.pop_back();
                                return false;
                            }

                            project.m_headerFiles.pop_back();
                            project.m_exportMacro = header.m_exportMacro;
                            return true;
                        }
                    }
                }
            }
        }

        //-------------------------------------------------------------------------

        project.m_headerFiles.pop_back();
        return true;
    }

