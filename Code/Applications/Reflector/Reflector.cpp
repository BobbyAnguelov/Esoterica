    bool Reflector::Reflect( Output desiredOutput, Operation operation )
    {
        if ( operation == Operation::HotReload )
        {
            EE_ASSERT( desiredOutput == Output::Shaders );
        }

        // Clean
        //-------------------------------------------------------------------------

        if ( operation == Operation::Clean || operation == Operation::Rebuild )
        {
            if ( !CleanOutputs( desiredOutput ) )
            {
                return false;
            }
        }

        // Build
        //-------------------------------------------------------------------------

        if ( operation == Operation::IncrementalBuild || operation == Operation::Rebuild || operation == Operation::HotReload )
        {
            // Shaders
            //-------------------------------------------------------------------------

            if ( IsShaderOutputRequired( desiredOutput ) )
            {
                if ( !ReflectShaders( desiredOutput ) )
                {
                    return false;
                }

                // Did the shader reflection result in changes to the engine types requiring us to re-run reflection?
                if ( m_shaderReflectionRequiresTypeReflection )
                {
                    if ( operation == Operation::HotReload )
                    {
                        return PrintError( "Shader Hot-Reload will result in changes to the engine runtime types requiring type reflection. Aborting Hot-Reload!" );
                    }

                    if ( !IsTypeInfoOutputRequired( desiredOutput ) )
                    {
                        // If this was a rebuild, we need to clean all typeinfo
                        if ( operation == Operation::Rebuild )
                        {
                            CleanOutputs( Output::TypeInfo, true );
                        }

                        desiredOutput = Output::ShadersAndTypeInfo;
                    }
                }

                if ( !WriteGeneratedFiles( Output::Shaders ) )
                {
                    return false;
                }
            }

            // TypeInfo
            //-------------------------------------------------------------------------

            if ( IsTypeInfoOutputRequired( desiredOutput ) )
            {
                if ( !ReflectTypeInfo() )
                {
                    return false;
                }

                if ( !WriteGeneratedFiles( Output::TypeInfo ) )
                {
                    return false;
                }
            }
        }

        // Bump Project Files
        //-------------------------------------------------------------------------

        for ( ReflectedProject& project : m_projects )
        {
            if ( project.m_requiresReload )
            {
                String projectFileContents;
                if ( FileSystem::ReadTextFile( project.m_path, projectFileContents ) )
                {
                    if ( !FileSystem::WriteTextFile( project.m_path, projectFileContents ) )
                    {
                        return PrintError( " * Failed to re-save project file: %s", project.m_path.c_str() );
                    }
                }
                else
                {
                    return PrintError( " * Failed to re-save project file: %s", project.m_path.c_str() );
                }
            }
        }

        return true;
    }

    bool Reflector::CleanOutputs( Output desiredOutput, bool silentMode )
    {
        auto CleaningError = [silentMode] ()
        {
            if ( !silentMode )
            {
                std::cout << std::endl;
                std::cout << "  ** Cleaning Failed! **" << std::endl;
            }

            return false;
        };

        //-------------------------------------------------------------------------

        if ( !silentMode )
        {
            std::cout << std::endl;
            std::cout << ">> Cleaning" << std::endl;
            std::cout << "-----------------------------------------------------" << std::endl << std::endl;
        }

        //-------------------------------------------------------------------------

        TVector<FileSystem::Path> generatedFiles;

        // Delete all auto-generated directories for valid projects
        for ( ReflectedProject& prj : m_projects )
        {
            if ( !silentMode )
            {
                std::cout << "  Cleaning Project: " << prj.m_name.c_str() << std::endl;
            }

            if ( IsShaderOutputRequired( desiredOutput ) )
            {
                if ( !CleanDirectory( prj.m_autogeneratedShaderDirectoryPath, silentMode ) )
                {
                    return CleaningError();
                }

                if ( !CleanDirectory( prj.m_autogeneratedShaderReflectionDirectoryPath, silentMode ) )
                {
                    return CleaningError();
                }
            }

            if ( IsTypeInfoOutputRequired( desiredOutput ) )
            {
                if ( !CleanDirectory( prj.m_autogeneratedTypeInfoDirectoryPath, silentMode ) )
                {
                    return CleaningError();
                }
            }

            // Delete all auto-generated directories for excluded projects (just to be safe)
            if ( !prj.IsRelevantForReflection() )
            {
                if ( !CleanDirectory( prj.m_autogeneratedDirectoryPath, silentMode ) )
                {
                    return CleaningError();
                }
            }

            prj.m_requiresReload = true;
        }

        // Registration Files
        //-------------------------------------------------------------------------

        if ( IsShaderOutputRequired( desiredOutput ) )
        {
            if ( !CleanFile( m_shaderRegistrationHeaderFilePath, "Shader Registration Header", silentMode ) )
            {
                return CleaningError();
            }

            if ( !CleanFile( m_shaderRegistrationSourceFilePath, "Shader Registration Source", silentMode ) )
            {
                return CleaningError();
            }
        }

        if ( IsTypeInfoOutputRequired( desiredOutput ) )
        {
            if ( !CleanFile( m_runtimeTypeRegistrationFilePath, "Runtime Type Registration", silentMode ) )
            {
                return CleaningError();
            }

            if ( !CleanFile( m_toolsTypeRegistrationFilePath, "Tools Type Registration", silentMode ) )
            {
                return CleaningError();
            }

            if ( !CleanFile( m_typeinfoDataXMLFilePath, "XML Reflection Data", silentMode ) )
            {
                return CleaningError();
            }
        }

        //-------------------------------------------------------------------------

        if ( !silentMode )
        {
            std::cout << std::endl;
            std::cout << "  ** Cleaning Success! **" << std::endl;
        }

        return true;
    }

    bool Reflector::ReflectShaders( Output desiredOutput )
    {
        EE_ASSERT( IsShaderOutputRequired( desiredOutput ) );

        m_shaderReflectionRequiresTypeReflection = false;

        // Parse Shaders
        //-------------------------------------------------------------------------

        std::cout << std::endl;
        std::cout << ">> Parsing Shaders" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        Milliseconds time = 0;

        {
            ScopedTimer<PlatformClock> timer( time );

            for ( ReflectedProject& project : m_projects )
            {
                if ( project.m_isExcludedFromShaderReflection )
                {
                    continue;
                }

                std::cout << "  Project: " << project.m_name.c_str() << " - ";

                if ( !project.m_shaders.empty() )
                {
                    ShaderParser shaderParser( project.m_shaders );
                    if ( !shaderParser.ParseShaders() )
                    {
                        return false;
                    }

                    std::cout << "Parsed! ( " << project.m_shaders.size() << " shader(s) found! )" << std::endl;
                }
                else
                {
                    std::cout << "  Project Ignored! ( No shaders found! )" << std::endl;
                }
            }
        }

        std::cout << std::endl;
        std::cout << "  ** Shader Parsing Success! ( " << time.ToFloat() << "ms ) **" << std::endl;

        // Shader List
        //-------------------------------------------------------------------------

        std::cout << std::endl;
        std::cout << ">> Shaders" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        for ( ReflectedProject& project : m_projects )
        {
            for ( ReflectedShader& shader : project.m_shaders )
            {
                std::cout << "  Shader: " << shader.m_name.c_str() << std::endl;
            }
        }

        // Write shader reflection
        //-------------------------------------------------------------------------

        std::cout << std::endl;
        std::cout << ">> Shader Input Reflection" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        {
            ScopedTimer<PlatformClock> timer( time );
            ShaderInputReflector inputReflector( m_projects );
            if ( !inputReflector.Reflect() )
            {
                return PrintError( inputReflector.GetErrorMessage() );
            }
        }

        std::cout << "  ** Shader Input Reflection Success! ( " << time.ToFloat() << "ms ) **" << std::endl;

        // Compile shaders
        //-------------------------------------------------------------------------

        int32_t const numPhysicalCores = Threading::GetNumPhysicalCores();

        std::cout << std::endl;
        std::cout << ">> Compiling Shaders ( using " << numPhysicalCores << " cores )" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        {
            ScopedTimer<PlatformClock> timer( time );
            ShaderCompiler compiler( m_solutionDirectoryPath, m_projects, numPhysicalCores );
            if ( !compiler.CompileShaders() )
            {
                return PrintError( compiler.GetErrorMessage() );
            }

            if ( compiler.HasWarnings() )
            {
                std::cout << "  * Warnings Occurred:" << std::endl;

                for ( auto const& warning : compiler.GetWarnings() )
                {
                    std::cout << "    " << warning.c_str() << std::endl;
                }

            }
        }

        std::cout << "  ** Shader Compilation Success! ( " << time.ToFloat() << "ms ) **" << std::endl;

        // Generate Code
        //-------------------------------------------------------------------------

        ShaderCodeGenerator generator( *this );

        std::cout << std::endl;
        std::cout << ">> Generating Shader Code" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        {
            ScopedTimer<PlatformClock> codeGenTimer( time );
            generator.GenerateCodeForSolution();
        }

        // Dump Log
        for ( auto const& entry : generator.GetLogEntries() )
        {
            if ( entry.m_severity == Severity::Error )
            {
                PrintError( entry.m_message.c_str() );
            }
            else
            {
                PrintWarning( entry.m_message.c_str() );
            }
        }

        if ( generator.HasErrors() )
        {
            std::cout << "  ** Shader Code Generation Failed! **" << std::endl;
            return false;
        }
        else
        {
            std::cout << "  ** Shader Code Generation Success! ( " << time.ToFloat() << "ms ) **" << std::endl;
        }

        // Detect whether shader reflection requires a typeinfo generation step
        //-------------------------------------------------------------------------

        if ( desiredOutput == Output::Shaders )
        {
            for ( auto const& project : m_projects )
            {
                if ( project.m_isExcludedFromShaderReflection )
                {
                    continue;
                }

                if ( ShaderParameterFilesWereUpdated( project.m_autogeneratedShaderDirectoryPath, project.m_generatedShaderFiles ) )
                {
                    m_shaderReflectionRequiresTypeReflection = true;
                    break;
                }
            }
        }
        else // Gonna generate typeinfo anyways so it doesnt hurt
        {
            m_shaderReflectionRequiresTypeReflection = true;
        }

        return true;
    }

    bool Reflector::ReflectTypeInfo()
    {
        // Parse headers
        //-------------------------------------------------------------------------

        std::cout << std::endl;
        std::cout << ">> Parsing Headers" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        TVector<ReflectedHeader*> headersToParse;
        for ( ReflectedProject& project : m_projects )
        {
            if ( project.m_isExcludedFromTypeInfoReflection )
            {
                continue;
            }

            // Add auto-generated shader headers
            if ( !project.m_isExcludedFromShaderReflection )
            {
                TVector<FileSystem::Path> generatedShaderParameterFiles;

                if ( project.m_autogeneratedShaderDirectoryPath.Exists() )
                {
                    if ( !FileSystem::GetDirectoryContents( project.m_autogeneratedShaderDirectoryPath, generatedShaderParameterFiles, FileSystem::DirectoryReaderOutput::OnlyFiles, FileSystem::DirectoryReaderMode::NoRecursion, { "h" } ) )
                    {
                        PrintError( "Error occurred while trying to find auto-generated shader parameter files" );
                        return false;
                    }
                }

                for ( FileSystem::Path const& headerPath : generatedShaderParameterFiles )
                {
                    String const filename = headerPath.GetFilename();
                    if ( !StringUtils::EndsWith( filename.c_str(), Settings::g_reflectedParametersFilenameSuffixAndExtension ) )
                    {
                        continue;
                    }

                    if ( !ParseHeader( project, headerPath ) )
                    {
                        PrintError( "Error parsing header: %s", headerPath.c_str() );
                        return false;
                    }
                }
            }

            //-------------------------------------------------------------------------

            std::cout << "  Project: " << project.m_name.c_str() << " - ";

            if ( !project.m_moduleHeaderID.IsValid() )
            {
                std::cout << "Ignored! ( Module not registered! )" << std::endl;
                continue;
            }

            if ( project.m_headerFiles.empty() )
            {
                std::cout << "Ignored! ( No headers found! )" << std::endl;
                continue;
            }

            std::cout << "Parsed! ( " << project.m_headerFiles.size() << " header(s) found! )" << std::endl;

            // Add all dirty headers to the list of file to be parsed
            bool moduleHeaderAdded = false;
            for ( ReflectedHeader& headerInfo : project.m_headerFiles )
            {
                headersToParse.push_back( &headerInfo );
            }
        }

        //-------------------------------------------------------------------------

        if ( headersToParse.empty() )
        {
            std::cout << std::endl;
            std::cout << "  ** No headers found - Nothing to do! **" << std::endl;
            return true;
        }

        // Reflect C++
        //-------------------------------------------------------------------------

        std::cout << std::endl;
        std::cout << ">> Reflecting C++ Code - First Pass (With Dev Tools)" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        ReflectionDatabase database( m_projects );
        ClangParser clangParser( m_solutionDirectoryPath, &database );
        if ( !clangParser.Parse( headersToParse, ClangParser::DevToolsPass ) )
        {
            return PrintError( clangParser.GetErrorMessage().c_str() );
        }

        Milliseconds clangParsingTime = clangParser.GetParsingTime();
        Milliseconds clangVisitingTime = clangParser.GetVisitingTime();
        std::cout << "  ** Pass Complete! ( P:" << (float) clangParsingTime << "ms, V:" << (float) clangVisitingTime << "ms ) **" << std::endl;

        //-------------------------------------------------------------------------

        std::cout << std::endl;
        std::cout << ">> Reflecting C++ Code - Second Pass (No Dev Tools)" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        // Second parse to detect dev-only types
        if ( !clangParser.Parse( headersToParse, ClangParser::NoDevToolsPass ) )
        {
            return PrintError( clangParser.GetErrorMessage().c_str() );
        }

        clangParsingTime = clangParser.GetParsingTime();
        clangVisitingTime = clangParser.GetVisitingTime();
        std::cout << "  ** Pass Complete! ( P:" << (float) clangParsingTime << "ms, V:" << (float) clangVisitingTime << "ms ) **" << std::endl;

        // Update Database
        //-------------------------------------------------------------------------

        std::cout << std::endl;
        std::cout << ">> Update Type Reflection DB" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        bool databaseValidationSuccess = false;

        Milliseconds time = 0;
        {
            ScopedTimer<PlatformClock> timer( time );
            if ( database.ValidateDataFileRegistrations() )
            {
                if ( database.ProcessAndValidateReflectedProperties() )
                {
                    databaseValidationSuccess = true;
                }
            }
        }

        // Dump log
        for ( auto const& entry : database.GetLogEntries() )
        {
            if ( entry.m_severity == Severity::Error )
            {
                PrintError( entry.m_message.c_str() );
            }
            else
            {
                PrintWarning( entry.m_message.c_str() );
            }
        }

        if ( database.HasErrors() || database.HasWarnings() )
        {
            std::cout << std::endl;
        }

        if ( !databaseValidationSuccess )
        {
            std::cout << "  ** Database Validation Failed **" << std::endl;
            return false;
        }

        database.CleanupResourceHierarchy();

        std::cout << "  ** Database Updated Success! ( " << time.ToFloat() << "ms ) **" << std::endl;

        // Generate Code
        //-------------------------------------------------------------------------

        TypeInfoCodeGenerator generator( *this, database );

        std::cout << std::endl;
        std::cout << ">> Generating Type Info Code" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        {
            ScopedTimer<PlatformClock> timer( time );
            generator.GenerateCodeForSolution();
        }

        // Dump Log
        for ( auto const& entry : generator.GetLogEntries() )
        {
            if ( entry.m_severity == Severity::Error )
            {
                PrintError( entry.m_message.c_str() );
            }
            else
            {
                PrintWarning( entry.m_message.c_str() );
            }
        }

        if ( generator.HasErrors() )
        {
            std::cout << "  ** Type Info Code Generation Failed! **" << std::endl;
            return false;
        }
        else
        {
            std::cout << "  ** Type Info Code Generation Success! ( " << time.ToFloat() << "ms ) **" << std::endl;
        }

        return true;
    }

    bool Reflector::ShaderParameterFilesWereUpdated( FileSystem::Path const& directoryPath, TVector<GeneratedFile> const& expectedFiles ) const
    {
        EE_ASSERT( directoryPath.IsValid() && directoryPath.IsDirectoryPath() );

        //-------------------------------------------------------------------------

        if ( !directoryPath.Exists() )
        {
            return true;
        }

        // Get directory contents
        TVector<FileSystem::Path> directoryContents;
        if ( !FileSystem::GetDirectoryContents( directoryPath, directoryContents ) )
        {
            return true;
        }

        // Compare list of existing files with expected files and delete any files that shouldn't exist
        //-------------------------------------------------------------------------

        for ( FileSystem::Path const& existingFile : directoryContents )
        {
            if ( !VectorContains( expectedFiles, existingFile, [] ( GeneratedFile const& lhs, FileSystem::Path const& rhs ) { return lhs.m_path == rhs; } ) )
            {
                // If this was a parameters file, then we need to re-run the type reflection
                if ( IsShaderParametersFilename( existingFile ) )
                {
                    return true;
                }
            }
        }

        // Check the generated files for differences
        //-------------------------------------------------------------------------

        for ( GeneratedFile const& generatedFile : expectedFiles )
        {
            if ( IsShaderParametersFilename( generatedFile.m_path ) )
            {
                if ( !generatedFile.m_path.Exists() )
                {
                    return true;
                }

                if ( DoTextFileContentsDiffer( generatedFile.m_path, generatedFile.m_contents ) )
                {
                    return true;
                }
            }
        }

        return false;
    }

    bool Reflector::UpdateAutogeneratedDirectory( FileSystem::Path const& directoryPath, TVector<GeneratedFile> const& expectedFiles, bool& wereFilesAddedOrRemoved ) const
    {
        EE_ASSERT( directoryPath.IsValid() && directoryPath.IsDirectoryPath() );

        wereFilesAddedOrRemoved = false;

        //-------------------------------------------------------------------------

        if ( !directoryPath.EnsureDirectoryExists() )
        {
            return PrintError( "Failed to ensure that directory exists: %s", directoryPath.c_str() );
        }

        // Get directory contents
        TVector<FileSystem::Path> directoryContents;
        if ( !FileSystem::GetDirectoryContents( directoryPath, directoryContents ) )
        {
            return PrintError( "Failed to get directory contents: %s", directoryPath.c_str() );
        }

        // There should be no directories inside the auto-generated folders
        for ( int32_t i = int32_t( directoryContents.size() ) - 1; i >= 0; i-- )
        {
            if ( directoryContents[i].IsDirectoryPath() )
            {
                if ( !FileSystem::EraseDir( directoryContents[i] ) )
                {
                    return PrintError( " * Error erasing directory: %s", directoryContents[i].c_str() );
                }

                directoryContents.erase_unsorted( directoryContents.begin() + i );
            }
        }

        // Compare list of existing files with expected files and delete any files that shouldn't exist
        //-------------------------------------------------------------------------

        for ( FileSystem::Path const& existingFile : directoryContents )
        {
            if ( !VectorContains( expectedFiles, existingFile, [] ( GeneratedFile const& lhs, FileSystem::Path const& rhs ) { return lhs.m_path == rhs; } ) )
            {
                wereFilesAddedOrRemoved = true;

                if ( !FileSystem::EraseFile( existingFile ) )
                {
                    return PrintError( " * Error erasing file: %s", existingFile.c_str() );
                }
            }
        }

        // Write out any files that have actually changed
        //-------------------------------------------------------------------------

        for ( GeneratedFile const& generatedFile : expectedFiles )
        {
            if ( !generatedFile.m_path.Exists() )
            {
                wereFilesAddedOrRemoved = true;
            }

            if ( !FileSystem::UpdateTextFile( generatedFile.m_path, generatedFile.m_contents ) )
            {
                return PrintError( " * Error creating generated file: %s", generatedFile.m_path.c_str() );
            }
        }

        return true;
    }

    bool Reflector::WriteGeneratedFiles( Output desiredOutput )
    {
        EE_ASSERT( desiredOutput != Output::ShadersAndTypeInfo );

        auto WritingError = [] ()
        {
            std::cout << "  ** Writing Failed! **" << std::endl;
            return false;
        };

        //-------------------------------------------------------------------------

        Milliseconds fileWriteTime = 0;

        std::cout << std::endl;
        std::cout << ">> Writing Generated Files (" << GetStringForOutput( desiredOutput ) << ")" << std::endl;
        std::cout << "-----------------------------------------------------" << std::endl << std::endl;

        {
            ScopedTimer<PlatformClock> codeGenTimer( fileWriteTime );

            // Delete all auto-generated directories for excluded projects (just to be safe)
            //-------------------------------------------------------------------------

            for ( auto& project : m_projects )
            {
                if ( !project.IsRelevantForReflection() )
                {
                    if ( !CleanDirectory( project.m_autogeneratedDirectoryPath, false ) )
                    {
                        return WritingError();
                    }
                }
            }

            // Get list of files in all known auto-generated directories
            //-------------------------------------------------------------------------

            for ( ReflectedProject& prj : m_projects )
            {
                if ( IsShaderOutputRequired( desiredOutput ) )
                {
                    bool wereFilesAddedOrRemoved = false;
                    if ( !UpdateAutogeneratedDirectory( prj.m_autogeneratedShaderDirectoryPath, prj.m_generatedShaderFiles, wereFilesAddedOrRemoved ) )
                    {
                        return WritingError();
                    }

                    prj.m_requiresReload |= wereFilesAddedOrRemoved;
                }

                if ( IsTypeInfoOutputRequired( desiredOutput ) )
                {
                    bool wereFilesAddedOrRemoved = false;
                    if ( !UpdateAutogeneratedDirectory( prj.m_autogeneratedTypeInfoDirectoryPath, prj.m_generatedTypeInfoFiles, wereFilesAddedOrRemoved ) )
                    {
                        return WritingError();
                    }

                    prj.m_requiresReload |= wereFilesAddedOrRemoved;
                }
            }

            // Write misc files out
            //-------------------------------------------------------------------------

            for ( auto const& generatedFile : m_miscGeneratedFiles )
            {
                if ( !FileSystem::UpdateTextFile( generatedFile.m_path, generatedFile.m_contents ) )
                {
                    return WritingError();
                }
            }
        }

        //-------------------------------------------------------------------------

        std::cout << "  ** Writing of Generated Files Complete! ( " << (float) fileWriteTime << "ms ) **" << std::endl;
        return true;
    }

