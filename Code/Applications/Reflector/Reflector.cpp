    //-------------------------------------------------------------------------

    void Reflector::AddGeneratedFile( FileSystem::Path const& path, char const* pFileContents )
    {
        for ( auto& project : m_projects )
        {
            if ( path.IsUnderDirectory( project.m_autogeneratedShaderDirectoryPath ) )
            {
                GeneratedFile& file = project.m_generatedShaderFiles.emplace_back();
                file.m_contents = pFileContents;
                file.m_path = path;
                return;
            }

            if ( path.IsUnderDirectory( project.m_autogeneratedTypeInfoDirectoryPath ) )
            {
                GeneratedFile& file = project.m_generatedTypeInfoFiles.emplace_back();
                file.m_contents = pFileContents;
                file.m_path = path;
                return;
            }
        }

        GeneratedFile& file = m_miscGeneratedFiles.emplace_back();
        file.m_contents = pFileContents;
        file.m_path = path;
    }
