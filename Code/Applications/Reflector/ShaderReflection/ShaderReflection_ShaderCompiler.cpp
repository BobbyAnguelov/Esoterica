    bool ShaderCompiler::CompileShaderStage( ReflectedShader& shader, String const& shaderStageID, ComPtr<IDxcCompiler3> &compiler, ComPtr<IDxcIncludeHandler> &includeHandler, TInlineVector<wchar_t const*, 20>& args ) const
    {
        DxcBuffer sourceBuffer = {};
        sourceBuffer.Ptr = shader.m_rawSourceText.c_str();
        sourceBuffer.Size = shader.m_rawSourceText.size();
        sourceBuffer.Encoding = 0;

        //-------------------------------------------------------------------------

        ComPtr<IDxcResult> dxcResult = {};
        if ( FAILED( compiler->Compile( &sourceBuffer, args.data(), UINT( args.size() ), includeHandler.Get(), IID_PPV_ARGS( dxcResult.ReleaseAndGetAddressOf() ) ) ) )
        {
            return LogError( "Compilation of '%s' failed", shader.m_path.c_str() );
        }

        HRESULT compileResult = S_OK;
        if ( FAILED( dxcResult->GetStatus( &compileResult ) ) )
        {
            return LogError( "Compilation of '%s' failed: unknown result", shader.m_path.c_str() );
        }

        if ( FAILED( compileResult ) )
        {
            ComPtr<IDxcBlobEncoding> errorBlob = {};
            if ( FAILED( dxcResult->GetErrorBuffer( errorBlob.ReleaseAndGetAddressOf() ) ) )
            {
                return false;
            }

            return LogError( "Compilation of '%s' failed: %hs", shader.m_path.c_str(), errorBlob->GetBufferPointer() );
        }

        // Try get warnings
        ComPtr<IDxcBlobUtf8> warningBlob;
        dxcResult->GetOutput( DXC_OUT_ERRORS, IID_PPV_ARGS( warningBlob.GetAddressOf() ), nullptr );
        if ( warningBlob && warningBlob->GetStringLength() > 0 )
        {
            LogWarning( "Compilation of '%s' succeeded with warnings: %hs", shader.m_path.c_str(), warningBlob->GetBufferPointer() );
        }

        //-------------------------------------------------------------------------

        ComPtr<IDxcBlob> compiledBlob = {};
        if ( FAILED( dxcResult->GetResult( compiledBlob.ReleaseAndGetAddressOf() ) ) )
        {
            return LogError( "Failed to retrieve compiled shader data for '%s'", shader.m_path.c_str() );
        }

        shader.m_compiledData.emplace_back( shaderStageID, static_cast<uint8_t const*>( compiledBlob->GetBufferPointer() ), compiledBlob->GetBufferSize() );

        return true;
    }

    bool ShaderCompiler::CompileMaterialShader( ReflectedShader& shader, ComPtr<IDxcCompiler3> &compiler, ComPtr<IDxcIncludeHandler> &includeHandler ) const
    {
        WString const shaderFilename( WString::CtorConvert(), shader.m_path.GetFilename().c_str() );
        WString const shaderDir( WString::CtorConvert(), shader.m_path.GetParentDirectory().c_str() );

        // MS/VS
        //-------------------------------------------------------------------------

        TInlineVector<wchar_t const*, 20> argumentsVS =
        {
            COMMON_DXC_ARGUMENTS( shaderFilename, shaderDir, m_codeDirectoryPath ),
            DXC_ARG_ENTRY_POINT( L"MS_main" ),
            DXC_ARG_TARGET( L"ms_6_6" )
        };

        if ( !CompileShaderStage( shader, "MS", compiler, includeHandler, argumentsVS ) )
        {
            return false;
        }

        shader.m_shaderStageToolMetadata.emplace_back( "MS_main", "MS", "" );

        // PS Default
        //-------------------------------------------------------------------------

        TInlineVector<wchar_t const*, 20> argumentsPS =
        {
            COMMON_DXC_ARGUMENTS( shaderFilename, shaderDir, m_codeDirectoryPath ),
            DXC_ARG_ENTRY_POINT( L"PS_main" ),
            DXC_ARG_TARGET( L"ps_6_6" ),
            DXC_ARG_DEFINE( L"PERMUTATION_DEFAULT" )
        };

        if ( !CompileShaderStage( shader, "PS_Default", compiler, includeHandler, argumentsPS ) )
        {
            return false;
        }

        shader.m_shaderStageToolMetadata.emplace_back( "PS_main", "PS", "PERMUTATION_DEFAULT;" );

        // PS AlphaTest
        //-------------------------------------------------------------------------

        TInlineVector<wchar_t const*, 20> argumentsPS_AT =
        {
            COMMON_DXC_ARGUMENTS( shaderFilename, shaderDir, m_codeDirectoryPath ),
            DXC_ARG_ENTRY_POINT( L"PS_main" ),
            DXC_ARG_TARGET( L"ps_6_6" ),
            DXC_ARG_DEFINE( L"PERMUTATION_ALPHA_TEST" )
        };

        if ( !CompileShaderStage( shader, "PS_AlphaTest", compiler, includeHandler, argumentsPS_AT ) )
        {
            return false;
        }

        shader.m_shaderStageToolMetadata.emplace_back( "PS_main", "PS", "PERMUTATION_ALPHA_TEST;" );

        // PS DepthOnly_AlphaTest
        //-------------------------------------------------------------------------

        TInlineVector<wchar_t const*, 20> argumentsPS_DO_AT =
        {
            COMMON_DXC_ARGUMENTS( shaderFilename, shaderDir, m_codeDirectoryPath ),
            DXC_ARG_ENTRY_POINT( L"PS_main" ),
            DXC_ARG_TARGET( L"ps_6_6" ),
            DXC_ARG_DEFINE( L"PERMUTATION_DEPTH_ONLY" ),
            DXC_ARG_DEFINE( L"PERMUTATION_ALPHA_TEST" )

        };

        if ( !CompileShaderStage( shader, "PS_DepthOnly_AlphaTest", compiler, includeHandler, argumentsPS_DO_AT ) )
        {
            return false;
        }

        shader.m_shaderStageToolMetadata.emplace_back( "PS_main", "PS", "PERMUTATION_DEFAULT;PERMUTATION_ALPHA_TEST;" );

        return true;
    }

    bool ShaderCompiler::CompileSurfaceShader( ReflectedShader& shader, ComPtr<IDxcCompiler3> &compiler, ComPtr<IDxcIncludeHandler> &includeHandler ) const
    {
        WString const shaderFilename( WString::CtorConvert(), shader.m_path.GetFilename().c_str() );
        WString const shaderDir( WString::CtorConvert(), shader.m_path.GetParentDirectory().c_str() );

        //-------------------------------------------------------------------------

        if ( !shader.m_useMeshShader )
        {
            TInlineVector<wchar_t const*, 20> argumentsVS =
            {
                COMMON_DXC_ARGUMENTS( shaderFilename, shaderDir, m_codeDirectoryPath ),
                DXC_ARG_ENTRY_POINT( L"VS_main" ),
                DXC_ARG_TARGET( L"vs_6_6" )
            };

            if ( !CompileShaderStage( shader, "VS", compiler, includeHandler, argumentsVS ) )
            {
                return false;
            }

            shader.m_shaderStageToolMetadata.emplace_back( "VS_main", "VS", "" );
        }
        else
        {
            TInlineVector<wchar_t const*, 20> argumentsMS =
            {
                COMMON_DXC_ARGUMENTS( shaderFilename, shaderDir, m_codeDirectoryPath ),
                DXC_ARG_ENTRY_POINT( L"MS_main" ),
                DXC_ARG_TARGET( L"ms_6_6" )
            };

            if ( !CompileShaderStage( shader, "MS", compiler, includeHandler, argumentsMS ) )
            {
                return false;
            }

            shader.m_shaderStageToolMetadata.emplace_back( "MS_main", "MS", "" );

            if ( shader.m_useTaskShader )
            {
                TInlineVector<wchar_t const*, 20> argumentsTS =
                {
                    COMMON_DXC_ARGUMENTS( shaderFilename, shaderDir, m_codeDirectoryPath ),
                    DXC_ARG_ENTRY_POINT( L"TS_main" ),
                    DXC_ARG_TARGET( L"as_6_6" )
                };

                if ( !CompileShaderStage( shader, "TS", compiler, includeHandler, argumentsTS ) )
                {
                    return false;
                }

                shader.m_shaderStageToolMetadata.emplace_back( "TS_main", "TS", "" );
            }
        }

        //-------------------------------------------------------------------------

        TInlineVector<wchar_t const*, 20> argumentsPS =
        {
            COMMON_DXC_ARGUMENTS( shaderFilename, shaderDir, m_codeDirectoryPath ),
            DXC_ARG_ENTRY_POINT( L"PS_main" ),
            DXC_ARG_TARGET( L"ps_6_6" )
        };

        if ( !CompileShaderStage( shader, "PS", compiler, includeHandler, argumentsPS ) )
        {
            return false;
        }

        shader.m_shaderStageToolMetadata.emplace_back( "PS_main", "PS", "" );

        return true;
    }

    bool ShaderCompiler::CompileComputeShader( ReflectedShader& shader, ComPtr<IDxcCompiler3> &compiler, ComPtr<IDxcIncludeHandler> &includeHandler ) const
    {
        DxcBuffer sourceBuffer = {};
        sourceBuffer.Ptr = shader.m_rawSourceText.c_str();
        sourceBuffer.Size = shader.m_rawSourceText.size();
        sourceBuffer.Encoding = 0;

        //-------------------------------------------------------------------------

        WString const shaderFilename( WString::CtorConvert(), shader.m_path.GetFilename().c_str() );
        WString const shaderDir( WString::CtorConvert(), shader.m_path.GetParentDirectory().c_str() );

        TInlineVector<wchar_t const*, 20> dxcArguments =
        {
            COMMON_DXC_ARGUMENTS( shaderFilename, shaderDir, m_codeDirectoryPath ),
            DXC_ARG_ENTRY_POINT( L"CS_main" ),
            DXC_ARG_TARGET( L"cs_6_6" )
        };

        if ( !CompileShaderStage( shader, "CS", compiler, includeHandler, dxcArguments ) )
        {
            return false;
        }

        shader.m_shaderStageToolMetadata.emplace_back( "CS_main", "CS", "" );

        return true;
    }

    void ShaderCompiler::CompilationTask( TaskSetPartition range, uint32_t threadNum )
    {
        ComPtr<IDxcUtils> dxcUtils = {};
        HRESULT hr = DxcCreateInstance( CLSID_DxcUtils, IID_PPV_ARGS( dxcUtils.ReleaseAndGetAddressOf() ) );
        if ( FAILED( hr ) )
        {
            LogError( "DXC utils creation failed with hr = %d", hr );
            return;
        }

        ComPtr<IDxcIncludeHandler> dxcIncludeHandler = {};
        hr = dxcUtils->CreateDefaultIncludeHandler( dxcIncludeHandler.ReleaseAndGetAddressOf() );
        if ( FAILED( hr ) )
        {
            LogError( "DXC Include handler creation failed with hr = %d", hr );
            return;
        }

        ComPtr<IDxcCompiler3> dxcCompiler = {};
        hr = DxcCreateInstance( CLSID_DxcCompiler, IID_PPV_ARGS( dxcCompiler.ReleaseAndGetAddressOf() ) );
        if ( FAILED( hr ) )
        {
            LogError( "DXC compiler creation failed with hr = %d", hr );
            return;
        }

        // Compile all shaders in range
        //-------------------------------------------------------------------------

        for ( uint32_t i = range.start; i < range.end; i++ )
        {
            ReflectedShader& shader = *m_shadersToCompile[i].m_pShader;

            bool shaderCompilationSuccess = false;

            switch ( shader.m_type )
            {
                case Render::ShaderType::Material:
                {
                    shaderCompilationSuccess = CompileMaterialShader( shader, dxcCompiler, dxcIncludeHandler );
                }
                break;

                case Render::ShaderType::Surface:
                {
                    shaderCompilationSuccess = CompileSurfaceShader( shader, dxcCompiler, dxcIncludeHandler );
                }
                break;

                case Render::ShaderType::Compute:
                {
                    shaderCompilationSuccess = CompileComputeShader( shader, dxcCompiler, dxcIncludeHandler );
                }
                break;

                default:
                {
                    EE_UNREACHABLE_CODE();
                    return;
                }
                break;
            }

            // Early out on failure
            if ( !shaderCompilationSuccess )
            {
                return;
            }
        }
    }
