    ShaderCompiler::ShaderCompiler( FileSystem::Path const& solutionDirectoryPath, TVector<ReflectedProject>& projects, int32_t numCoresToUse )
        : m_projects( projects )
        , m_taskSystem( numCoresToUse )
        , m_compilationTask( [this] ( TaskSetPartition range, uint32_t threadNum ) { CompilationTask( range, threadNum ); } )
        , m_codeDirectoryPath( WString::CtorConvert(), solutionDirectoryPath.GetAppended( Settings::g_codeFolderPath, true ).c_str() )
    {
        m_taskSystem.Initialize();
    }

    ShaderCompiler::~ShaderCompiler()
    {
        m_taskSystem.WaitForAll();
        m_taskSystem.Shutdown();
    }

    bool ShaderCompiler::LogError( char const* pFormat, ... ) const
    {
        Threading::ScopeLock const l( m_mutex );

        va_list args;
        va_start( args, pFormat );
        m_errorMessage.append_sprintf_va_list( pFormat, args );
        m_errorMessage.append( "\n" );
        va_end( args );

        return false;
    }

    void ShaderCompiler::LogWarning( char const* pFormat, ... ) const
    {
        Threading::ScopeLock const l( m_mutex );

        va_list args;
        va_start( args, pFormat );
        String& str = m_warnings.emplace_back();
        str.append_sprintf_va_list( pFormat, args );
        va_end( args );
    }

    bool ShaderCompiler::CompileShaders()
    {
        // Generate list of shaders to compile
        //-------------------------------------------------------------------------

        m_shadersToCompile.clear();

        for ( ReflectedProject& project : m_projects )
        {
            if ( project.m_isExcludedFromShaderReflection )
            {
                continue;
            }

            for ( ReflectedShader& shader : project.m_shaders )
            {
                auto& item = m_shadersToCompile.emplace_back();
                item.m_pShader = &shader;
                item.m_outputPath = project.m_autogeneratedDirectoryPath;
            }
        }

        // Kick off task
        //-------------------------------------------------------------------------

        #if 0 // Single Threaded compilation for debug
        CompilationTask( TaskSetPartition( 0, (uint32_t) m_shadersToCompile.size() ), 0 );
        #else
        m_compilationTask.m_SetSize = (uint32_t) m_shadersToCompile.size();
        m_taskSystem.ScheduleTask( &m_compilationTask );
        m_taskSystem.WaitForAll();
        #endif

        return !HasErrors();
    }

