    ShaderInputReflector::ShaderInputReflector( TVector<ReflectedProject> const& projects )
        : m_projects( projects )
    {}

    bool ShaderInputReflector::Reflect()
    {
        for ( ReflectedProject const& project : m_projects )
        {
            project.m_autogeneratedShaderReflectionDirectoryPath.EnsureDirectoryExists();

            for ( ReflectedShader const& shader : project.m_shaders )
            {
                GenerateReflectedInputsForShader( project.m_autogeneratedShaderReflectionDirectoryPath, shader );
            }
        }

        return WriteGeneratedFiles();
    }

    void ShaderInputReflector::GenerateReflectedInputsForShader( FileSystem::Path const& outputDirectoryPath, ReflectedShader const& shader )
    {
        std::stringstream stream;

        stream << "//-------------------------------------------------------------------------\n";
        stream << "// This is an auto-generated file - DO NOT edit\n";
        stream << "//-------------------------------------------------------------------------\n";
        stream << "// Shader input reflection for: \"" << shader.m_path.c_str() << "\"\n\n";

        auto WriteReflectedStruct = [&stream] ( String const& structName, TVector<ReflectedShader::ParameterInfo> const& members )
        {
            stream << "struct HLSL_ALIGN_STRUCT " << structName.c_str() << "Data\n";
            stream << "{\n";

            size_t structureSize = 0;
            for ( ReflectedShader::ParameterInfo const& parameter : members )
            {
                if ( parameter.m_type == "esf_padding_t" || parameter.m_type == "TBitFlags<MaterialShaderFlags>" )
                {
                    stream << "    uint " << parameter.m_name.c_str() << ";\n";
                }
                else
                {
                    if ( parameter.m_strideInBytes == 2 )
                    {
                        EE_ASSERT( parameter.m_isPacked );

                        uint32_t packMemberIndex = ( parameter.m_offsetInBytes / 4 ) * 4;
                        if ( ( parameter.m_offsetInBytes % 4 ) == 0 )
                        {

                            stream << "    uint m_packedData" << packMemberIndex << ";\n";
                            stream << "    void Set" << parameter.m_friendlyName.c_str() << "( uint16_t value ) { m_packedData" << packMemberIndex << " &= ~0xFFFF; m_packedData" << packMemberIndex << " |= uint( value ) & 0x0000FFFF; }\n";
                            stream << "    uint16_t Get" << parameter.m_friendlyName.c_str() << "() { return uint16_t( m_packedData" << packMemberIndex << " & 0xFFFF ); }\n";
                        }
                        else
                        {
                            stream << "    void Set" << parameter.m_friendlyName.c_str() << "( uint16_t value ) { m_packedData" << packMemberIndex << " &= 0xFFFF; m_packedData" << packMemberIndex << " |= uint( value ) << 16; }\n";
                            stream << "    uint16_t Get" << parameter.m_friendlyName.c_str() << "() { return uint16_t( m_packedData" << packMemberIndex << " >> 16 ); }\n";
                        }
                    }
                    else if ( parameter.m_strideInBytes == 8 )
                    {
                        EE_ASSERT( parameter.m_isPacked );
                        EE_ASSERT( parameter.m_offsetInBytes % 4 == 0 );

                        uint32_t packMemberIndex0 = ( parameter.m_offsetInBytes / 4 ) * 4;
                        uint32_t packMemberIndex1 = packMemberIndex0 + 4;

                        stream << "    uint m_packedData" << packMemberIndex0 << ";\n";
                        stream << "    uint m_packedData" << packMemberIndex1 << ";\n";

                        stream
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( uint64_t value ) { m_packedData" << packMemberIndex0 << " = uint( value >> 32 ); m_packedData" << packMemberIndex1 << " = uint( value & 0xFFFFFFFF ); }\n"
                            << "    uint64_t Get" << parameter.m_friendlyName.c_str() << "() { return ( uint64_t( m_packedData" << packMemberIndex0 << " ) << 32 ) | uint64_t( m_packedData" << packMemberIndex1 << " ); }\n";
                    }
                    else
                    {
                        EE_ASSERT( !parameter.m_isPacked );

                        stream << "    " << parameter.m_type.c_str() << " " << parameter.m_name.c_str() << ";\n";
                    }

                    //-------------------------------------------------------------------------

                    if ( parameter.m_type == "AppendBuffer" || parameter.m_type == "RawAppendBuffer" )
                    {
                        stream
                            << "#ifdef __cplusplus\n"
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<EE::Render::RHI::PipelineStage> stages, DeviceAppendBufferBase const& " << parameter.m_friendlyName.c_str() << " );\n"
                            << "#endif\n";
                    }
                    else if ( parameter.m_type == "Buffer" || parameter.m_type == "StructuredBuffer" || parameter.m_type == "ByteAddressBuffer" )
                    {
                        stream
                            << "#ifdef __cplusplus\n"
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<EE::Render::RHI::PipelineStage> stages, DeviceBufferState& " << parameter.m_friendlyName.c_str() << " );\n"
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::Buffer* p" << parameter.m_friendlyName.c_str() << " );\n"
                            << "#endif\n";
                    }
                    else if ( parameter.m_type == "RWBuffer" || parameter.m_type == "RWStructuredBuffer" || parameter.m_type == "RWByteAddressBuffer" )
                    {
                        stream
                            << "#ifdef __cplusplus\n"
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<EE::Render::RHI::PipelineStage> stages, DeviceBufferState& " << parameter.m_friendlyName.c_str() << " );\n"
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::Buffer* p" << parameter.m_friendlyName.c_str() << " );\n"
                            << "#endif\n";
                    }
                    else if ( parameter.m_type == "Texture1D" || parameter.m_type == "Texture2D" || parameter.m_type == "Texture3D" || parameter.m_type == "TextureCube" )
                    {
                        stream
                            << "#ifdef __cplusplus\n"
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<RHI::PipelineStage> stages, DeviceTextureState& " << parameter.m_friendlyName.c_str() << " );\n"
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::Texture* p" << parameter.m_friendlyName.c_str() << " );\n"
                            << "#endif\n";
                    }
                    else if ( parameter.m_type == "RWTexture1D" || parameter.m_type == "RWTexture2D" || parameter.m_type == "RWTexture3D" || parameter.m_type == "RWTextureCube" )
                    {
                        stream
                            << "#ifdef __cplusplus\n"
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<EE::Render::RHI::PipelineStage> stages, DeviceTextureState& " << parameter.m_friendlyName.c_str() << ", uint32_t mipLevel );\n"
                            << "    void Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::Texture* p" << parameter.m_friendlyName.c_str() << ", uint32_t mipLevel );\n"
                            << "#endif\n";
                    }
                }

                structureSize = parameter.m_offsetInBytes + parameter.m_strideInBytes;
            }

            if ( ( structureSize % 4 ) != 0 )
            {
                // means that the struct has free 16 bits in the last packed member, just adjust the offset
                EE_ASSERT( ( structureSize % 2 ) == 0 );
                structureSize += 2;
            }

            stream << "};\n\n";

            // HLSL load / create function

            stream << "#ifndef __cplusplus\n";
            stream << "#define " << structName.c_str() << "RootConstant(B, S) \"RootConstants( \" B \", num32BitConstants = " << structureSize / 4 << ", \" S \" ),\" \n";
            stream << structName.c_str() << " Create" << structName.c_str() << "( " << structName.c_str() << "Data data )\n";
            stream << "{\n";
            stream << "    " << structName.c_str() << " result;\n";

            for ( ReflectedShader::ParameterInfo const& parameter : members )
            {
                if ( parameter.m_type == "esf_padding_t" )
                {
                    // skip padding
                }
                else if ( parameter.m_type == "SamplerState" )
                {
                    EE_ASSERT( parameter.m_isPacked );

                    stream << "    result." << parameter.m_name.c_str() << " = SamplerDescriptorHeap[NonUniformResourceIndex( data.Get" << parameter.m_friendlyName.c_str() << "() )];\n";
                }
                else if ( parameter.m_isHandle )
                {
                    EE_ASSERT( parameter.m_isPacked );

                    if ( parameter.m_type == "AppendBuffer" )
                    {
                        stream << "    result." << parameter.m_name.c_str() << " = CreateAppendBuffer<" << parameter.m_templateArguments[0].c_str() << ">( data.Get" << parameter.m_friendlyName.c_str() << "() );\n";
                    }
                    else if ( parameter.m_type == "RawAppendBuffer" )
                    {
                        stream << "    result." << parameter.m_name.c_str() << " = CreateRawAppendBuffer( data.Get" << parameter.m_friendlyName.c_str() << "() );\n";
                    }
                    else
                    {
                        stream << "    result." << parameter.m_name.c_str() << " = ResourceDescriptorHeap[NonUniformResourceIndex( data.Get" << parameter.m_friendlyName.c_str() << "() )];\n";
                    }
                }
                else
                {
                    if ( parameter.m_isPacked )
                    {
                        stream << "    result." << parameter.m_name.c_str() << " = data.Get" << parameter.m_friendlyName.c_str() << "();\n";
                    }
                    else
                    {
                        stream << "    result." << parameter.m_name.c_str() << " = data." << parameter.m_name.c_str() << ";\n";
                    }
                }
            }

            stream << "    return result;\n";
            stream << "}\n";

            stream << structName.c_str() << " Load" << structName.c_str() << "( ByteAddressBuffer shaderDataBuffer, uint offsetIn32ByteBlocks )\n";
            stream << "{\n";
            stream << "    " << structName.c_str() << "Data data = shaderDataBuffer.Load<" << structName.c_str() << "Data>( offsetIn32ByteBlocks * 32 );\n";
            stream << "    return Create" << structName.c_str() << "( data );\n";
            stream << "}\n";
            stream << "#endif\n";

            // C++ setter function

            stream << "#ifdef __cplusplus\n";
            for ( ReflectedShader::ParameterInfo const& parameter : members )
            {
                if ( parameter.m_type == "AppendBuffer" || parameter.m_type == "RawAppendBuffer" )
                {
                    stream
                        << "void " << structName.c_str() << "Data::Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<EE::Render::RHI::PipelineStage> stages, DeviceAppendBufferBase const& " << parameter.m_friendlyName.c_str() << " )\n"
                        << "{\n"
                        << "    states.Writeable( " << parameter.m_friendlyName.c_str() << ".m_deviceCounterBuffer, stages, EE::Render::RHI::ResourceAccess::UnorderedAccess );\n"
                        << "    states.Writeable( " << parameter.m_friendlyName.c_str() << ".m_deviceBuffer, stages, EE::Render::RHI::ResourceAccess::UnorderedAccess ) ;\n"
                        << "    Set" << parameter.m_friendlyName.c_str() << "( " << parameter.m_friendlyName.c_str() << ".GetAppendBufferHandle() );\n"
                        << "}\n";
                }
                else if ( parameter.m_type == "Buffer" || parameter.m_type == "StructuredBuffer" || parameter.m_type == "ByteAddressBuffer" )
                {
                    stream
                        << "void " << structName.c_str() << "Data::Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<EE::Render::RHI::PipelineStage> stages, DeviceBufferState& " << parameter.m_friendlyName.c_str() << " )\n"
                        << "{\n"
                        << "    states.ReadOnly( " << parameter.m_friendlyName.c_str() << ", stages, EE::Render::RHI::ResourceAccess::ShaderResource );\n"
                        << "    Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::GetBufferHandle( " << parameter.m_friendlyName.c_str() << ", RHI::DescriptorTypeFlags::Buffer ) );\n"
                        << "}\n"
                        << "void " << structName.c_str() << "Data::Set" << parameter.m_friendlyName.c_str() << "( RHI::Buffer* p" << parameter.m_friendlyName.c_str() << " )\n"
                        << "{\n"
                        << "    Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::GetBufferHandle( p" << parameter.m_friendlyName.c_str() << ", RHI::DescriptorTypeFlags::Buffer ) );\n"
                        << "}\n";
                }
                else if ( parameter.m_type == "RWBuffer" || parameter.m_type == "RWStructuredBuffer" || parameter.m_type == "RWByteAddressBuffer" )
                {
                    stream
                        << "void " << structName.c_str() << "Data::Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<EE::Render::RHI::PipelineStage> stages, DeviceBufferState& " << parameter.m_friendlyName.c_str() << " )\n"
                        << "{\n"
                        << "    states.Writeable( " << parameter.m_friendlyName.c_str() << ", stages, EE::Render::RHI::ResourceAccess::UnorderedAccess );\n"
                        << "    Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::GetBufferHandle( " << parameter.m_friendlyName.c_str() << ", RHI::DescriptorTypeFlags::RWBuffer ) );\n"
                        << "}\n"
                        << "void " << structName.c_str() << "Data::Set" << parameter.m_friendlyName.c_str() << "( RHI::Buffer* p" << parameter.m_friendlyName.c_str() << " )\n"
                        << "{\n"
                        << "    Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::GetBufferHandle( p" << parameter.m_friendlyName.c_str() << ", RHI::DescriptorTypeFlags::RWBuffer ) );\n"
                        << "}\n";
                }
                else if ( parameter.m_type == "Texture1D" || parameter.m_type == "Texture2D" || parameter.m_type == "Texture3D" || parameter.m_type == "TextureCube" )
                {
                    stream
                        << "void " << structName.c_str() << "Data::Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<EE::Render::RHI::PipelineStage> stages, DeviceTextureState& " << parameter.m_friendlyName.c_str() << " )\n"
                        << "{\n"
                        << "    states.ReadOnly( " << parameter.m_friendlyName.c_str() << ", stages, EE::Render::RHI::ResourceAccess::ShaderResource, EE::Render::RHI::TextureState::ShaderResource );\n"
                        << "    Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::GetTextureHandle( " << parameter.m_friendlyName.c_str() << ", RHI::DescriptorTypeFlags::Texture, 0 ) );\n"
                        << "}\n"
                        << "void " << structName.c_str() << "Data::Set" << parameter.m_friendlyName.c_str() << "( RHI::Texture* p" << parameter.m_friendlyName.c_str() << " )\n"
                        << "{\n"
                        << "    Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::GetTextureHandle( p" << parameter.m_friendlyName.c_str() << ", RHI::DescriptorTypeFlags::Texture, 0 ) );\n"
                        << "}\n";
                }
                else if ( parameter.m_type == "RWTexture1D" || parameter.m_type == "RWTexture2D" || parameter.m_type == "RWTexture3D" || parameter.m_type == "RWTextureCube" )
                {
                    stream
                        << "void " << structName.c_str() << "Data::Set" << parameter.m_friendlyName.c_str() << "( DeviceResourceStates& states, TBitFlags<EE::Render::RHI::PipelineStage> stages, DeviceTextureState& " << parameter.m_friendlyName.c_str() << ", uint32_t mipLevel )\n"
                        << "{\n"
                        << "    states.Writeable( " << parameter.m_friendlyName.c_str() << ", stages, EE::Render::RHI::ResourceAccess::UnorderedAccess, EE::Render::RHI::TextureState::UnorderedAccess );\n"
                        << "    Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::GetTextureHandle( " << parameter.m_friendlyName.c_str() << ", RHI::DescriptorTypeFlags::RWTexture, mipLevel ) );\n"
                        << "}\n"
                        << "void " << structName.c_str() << "Data::Set" << parameter.m_friendlyName.c_str() << "( RHI::Texture* p" << parameter.m_friendlyName.c_str() << ", uint32_t mipLevel )\n"
                        << "{\n"
                        << "    Set" << parameter.m_friendlyName.c_str() << "( EE::Render::RHI::GetTextureHandle( p" << parameter.m_friendlyName.c_str() << ", RHI::DescriptorTypeFlags::RWTexture, mipLevel ) );\n"
                        << "}\n";
                }
            }
            stream << "#endif\n";
        };

        stream << "EE_BEGIN_SHADER_TYPES_NAMESPACE\n";

        //-------------------------------------------------------------------------

        if ( !shader.m_customParametersStructName.empty() )
        {
            WriteReflectedStruct( shader.m_customParametersStructName, shader.m_parameters );
        }

        //-------------------------------------------------------------------------

        if ( !shader.m_resourceTableStructName.empty() )
        {
            WriteReflectedStruct( shader.m_resourceTableStructName, shader.m_resourceTable );
        }

        //-------------------------------------------------------------------------

        stream << "EE_END_SHADER_TYPES_NAMESPACE\n";

        GeneratedFile& file = m_generatedFiles.emplace_back();
        file.m_contents = stream.str().c_str();

        file.m_path = outputDirectoryPath;
        file.m_path.Append( shader.m_name );
        file.m_path.AppendExtension( "esh" );
    }

    bool ShaderInputReflector::WriteGeneratedFiles()
    {
        for ( auto const& generatedFile : m_generatedFiles )
        {
            if ( !FileSystem::UpdateTextFile( generatedFile.m_path, generatedFile.m_contents ) )
            {
                m_errorMessage.sprintf( "Error writing file: %s", generatedFile.m_path.c_str() );
                return false;
            }
        }

        return true;
    }
