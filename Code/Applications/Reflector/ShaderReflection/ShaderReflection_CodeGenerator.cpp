    void ShaderCodeGenerator::GenerateXML()
    {
        xml_document doc;

        xml_node decl = doc.append_child( node_declaration );
        decl.append_attribute( "version" ) = "1.0";
        decl.append_attribute( "encoding" ) = "UTF-8";

        //-------------------------------------------------------------------------

        xml_node headerComment = doc.append_child( node_comment );
        headerComment.set_value( "Esoterica Shader Stage Metadata" );

        headerComment = doc.append_child( node_comment );
        headerComment.set_value( "This is an autogenerated file - DO NOT edit" );

        //-------------------------------------------------------------------------

        xml_node rootNode = doc.root().append_child( "CompiledShaderStages" );

        for ( ReflectedProject const& project : m_reflector.GetProjects() )
        {
            if ( project.m_isExcludedFromShaderReflection )
            {
                continue;
            }

            for ( ReflectedShader const& shader : project.m_shaders )
            {
                for ( ReflectedShader::StageToolMetadata const& stageToolMetadata : shader.m_shaderStageToolMetadata )
                {
                    xml_node compiledShader = rootNode.append_child( "CompiledShader" );
                    compiledShader.append_attribute( "Name" ).set_value( shader.m_name.c_str() );
                    compiledShader.append_attribute( "FilePath" ).set_value( shader.m_path.c_str() );
                    compiledShader.append_attribute( "EntryPoint" ).set_value( stageToolMetadata.m_pEntryPoint );
                    compiledShader.append_attribute( "Stage" ).set_value( stageToolMetadata.m_pStage );
                    compiledShader.append_attribute( "CommandLineDXC" ).set_value( "-HV 2021 -enable-16bit-types" );

                    TVector<String> macroDefinitions;
                    StringUtils::Split( String( stageToolMetadata.m_pMacroDefinitions ), macroDefinitions, ";" );

                    for ( String const& name : macroDefinitions )
                    {
                        xml_node macro = compiledShader.append_child( "MacroDefinition" );
                        macro.append_attribute( "Name" ).set_value( name.c_str() );
                        macro.append_attribute( "Value" ).set_value( "1" );
                    }
                }
            }
        }

        std::stable_sort( doc.begin(), doc.end(), [] ( xml_node node0, xml_node node1 )
        {
            return stricmp( node0.attribute( "Name" ).as_string(), node1.attribute( "Name" ).as_string() );
        } );

        //-------------------------------------------------------------------------

        String fileContents;
        Serialization::WriteXmlToString( doc, fileContents );
        m_reflector.AddGeneratedFile( m_reflector.GetShaderMetadataXMLFilePath(), fileContents.c_str() );
    }
